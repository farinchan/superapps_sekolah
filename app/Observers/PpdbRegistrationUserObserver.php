                    "Tanggapan dari panitia PPDB: _" . $ppdbRegistrationUser->reason . "_\n \n" .
                    "Jika ada pertanyaan, silakan hubungi kami melalui WhatsApp atau email. \n" .
                    "Terima kasih, \n" .
                    "*Panitia PPDB* \n \n" .

                    "_generated by System_" . "\n" .
                    "https://ppdb.man1kotapadangpanjang.sch.id"
            ]);

            if ($response_wa->status() != 200) {
                Log::error('Failed to send message to ' . $ppdbRegistrationUser->user?->whatsapp_number);
            } else {
                Log::info('Message sent to ' . $ppdbRegistrationUser->user?->whatsapp_number);
            }
        } catch (\Exception $e) {
            Log::error($e->getMessage());
        }
    }

